## FAQ

### ¿Necesitamos un wedding planner en Portugal?
Para 30+ invitados y si no habláis portugués: **sí**, casi obligatorio. 1.500-3.500 € por su trabajo, ahorro real en estrés y errores de comunicación. Para 10-20 invitados podéis arreglaros si tenéis tiempo y nivel medio de inglés.

### ¿Pueden los invitados venir con niños?
Por supuesto. Las quintas portuguesas son family-friendly. Algunas tienen piscina y zonas verdes donde los niños se entretienen mientras los adultos disfrutan. Coordinad cuidador profesional si la fiesta se alarga (40-80 € por noche).

### ¿Y la comida? ¿Es buena?
La gastronomía portuguesa es excelente. Mariscos del Atlántico (almejas, pulpo, sardinas), carne de cerdo ibérico (sí, también), vinos del Douro. La estructura tradicional es entrante de mariscos / plato principal de carne o pescado / postres regionales (pastel de Belém en versión catering).

### ¿Cómo se transportan los invitados?
Si todos llegan al mismo aeropuerto (Faro, Lisboa, Porto), un autobús privado de 50 plazas cuesta 400-600 € para llevarlos al hotel y a la quinta. Más sencillo que coches de alquiler para grupos.

### ¿Y la barra libre?
Costumbre portuguesa: barra libre hasta las 2-3 de la madrugada con vino, cerveza, gin tonics. Spirits premium suelen ir aparte. Coordinable según presupuesto.

### Mi madre tiene 75 años y no vuela. ¿Hay tren?
Renfe + CP (compañía portuguesa) tienen Alvia Madrid-Lisboa 9h, lujoso y cómodo. Bilbao-Porto en coche 6h. Para invitados mayores siempre hay alternativas; el coche-cama Lusitania nocturno también existe.

