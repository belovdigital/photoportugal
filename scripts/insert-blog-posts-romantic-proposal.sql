## Porto: Riverside Romance and Port Wine

[Porto](/locations/porto) is a city with soul. Grittier than Lisbon, more authentic, and with a raw beauty that makes romance feel real rather than staged. The Douro River running through the heart of the city, spanned by dramatic bridges, creates a setting that''s both grand and intimate.

### Where to Go

The **Ribeira district** along the riverfront is Porto''s most romantic quarter. The colorful facades of centuries-old buildings reflect in the water, and in the evening, the whole scene is lit by the warm glow of riverside restaurants. Share a plate of francesinha and a glass of port while watching rabelo boats drift past.

