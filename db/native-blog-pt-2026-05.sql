## Erros comuns a evitar

- **Não atribuir um "responsável" pela logística**. Despedida sem MOH (maid of honour) organizadora = caos. Uma rapariga deve ser o "diretor de operações" — ela não bebe muito, conhece a hora dos restaurantes, evita cobranças surpresas.
- **Bebida pesada ANTES da sessão fotográfica**. Faça a sessão antes do bar. Após 2 cocktails todas começam a aparecer "vermelhas" nas fotos.
- **Marcar tudo no último mês**. Hotéis, fotógrafo, restaurantes — todos lotados em Junho-Setembro. **3-4 meses antes** é o mínimo.
- **Ignorar dietéticas/alergias**. Sempre ter na conversa do grupo "se alguém tem restrições, avisa-me até dia X".

## FAQ

### Quantas raparigas é o número ideal?
6-10. Menos de 6 é íntimo demais para "despedida". Mais de 12 é difícil de coordenar (transportes, restaurantes recusam mesa, foto é caos).

### Quanto custa em média uma despedida no Algarve?
Para 8 raparigas, 3 noites, programa completo (hotel decente + actividades + jantares + sessão fotos): 400-700€ por pessoa. Em villa partilhada baixa para 300-500€/pessoa.

### A noiva paga ou não?
Tradição portuguesa actual: **a noiva não paga nada**. As amigas dividem todos os custos da noiva (hotel, jantares, actividades, fotógrafo). Conversação prévia é importante para alinhar expectativas.

### Pode ser surpresa para a noiva?
Sim — chama-se "surprise bachelorette". A organizadora coordena o sigilio. A noiva sabe que vai a Algarve mas não conhece o programa. Funciona muito bem se ela é o tipo "gosta de surpresas". Para tipo controlador, melhor envolver na planificação.

### Conduzir vs táxi?
Em Lagos/Vilamoura, **andar a pé + Uber** é o normal. Conduzir não vale a pena se vão estar a beber. Vilamoura tem Uber abundante. Lagos nem sempre — apostem em transferências privadas pré-marcadas.

### Vamos casar nós próprias 2 anos depois — vale a pena guardar contactos?
Absolutamente. Bons fotógrafos do Algarve trabalham despedidas, casamentos e elopements. Adicionem-no(a) ao Instagram e voltem em 2 anos.

