## Quando fotografar?

**Setembro-Outubro**: o melhor mês do ano para fotografias no Porto. Luz suave, temperatura amena (18-22°C), sem chuva pesada, vegetação ainda verde. Domingos disponíveis em todos os fotógrafos.

