## Que vestir?

A vossa avó vai dizer "não, isso não, ponham os fatos de jantar". Resistam. Os fotos onde toda a família está formal-rígida-com-gravata raramente envelhecem bem. As fotos com calças de ganga + camisola elegante envelhecem maravilhosamente.

**Regras simples**:
- **Coordenar, não combinar**. 3 cores comuns (ex: bege + branco + azul-marinho), cada um veste a sua interpretação.
- **Tons neutros e terrosos** funcionam sempre no Porto (cidade já tem muito laranja-tijolo + azul-azulejo).
- **Evitar logos** visíveis e padrões fortes (riscas pretas e brancas, camisas com letras, T-shirts de marcas).
- **Sapatos confortáveis** — vais andar 1-2 km durante a sessão.
