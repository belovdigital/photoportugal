Just **30 minutes by train from Lisbon**, Cascais is the Portuguese Riviera — elegant, relaxed, and incredibly photogenic. This former fishing village turned glamorous beach town offers a completely different vibe from Lisbon''s urban energy. Think pastel-colored mansions, turquoise coves, rugged Atlantic cliffs, and a laid-back coastal atmosphere that makes every photo feel effortless.

What makes [Cascais](/locations/cascais) special for photoshoots is the variety packed into a compact, walkable area. Within a single session, your photographer can capture you against a marina backdrop, dramatic ocean cliffs, charming cobblestone streets, and a pristine beach — all without getting in a car.

For families, Cascais offers calmer beaches and wider sidewalks than Lisbon. For couples, the sunset over the Atlantic is one of the most romantic moments you''ll experience in Portugal. And for solo travelers, the coastal elegance creates a backdrop that looks like a European travel magazine.

## Best Photo Spots in Cascais

### Cascais Marina

The marina is one of the most photogenic spots in town. Rows of white sailboats bob in crystal-clear water, with the colorful town rising behind them. The light here is beautiful in the morning when the water is calm and reflections are sharp. Walk along the boardwalk for wide shots, or step onto the dock for intimate portraits with boats in the background.

**Best time:** Morning for calm water and reflections

### Boca do Inferno (Hell''s Mouth)

This dramatic cliff formation is where the Atlantic crashes into a natural rock arch with tremendous force. The raw power of the ocean and the jagged cliff edges create an unforgettable backdrop — especially at high tide when waves explode through the cave. It''s a 15-minute walk along the coast from the marina.

**Best time:** Late afternoon for dramatic side-lighting on the cliffs

### Casa da Guia

