## A sessão em si

Realisticamente, esperem isto:

**Antes** (1 semana antes): conversa por WhatsApp ou telefone com fotógrafo. Combinam local, hora, vestuário, expectativas.

**Dia da sessão**:
- Chegam 10 min antes do horário.
- Primeiros 10-15 min: caminham, conversam, fotógrafo "aquece" — fotos não muito formais, vão soltando.
- Coração da sessão (40-50 min): sequências em diferentes ângulos, mãe com filhos, pai sozinho com cada criança, irmãos juntos, todos juntos, andando, parados, rindo.
- Últimos 10 min: fotos "individuais" se quiser (head-shot do filho mais velho, retrato da avó, etc).

**Depois**: galeria entregue por link com password 2-3 semanas depois. Recebem 30-50 fotos editadas em alta qualidade. Podem imprimir directamente ou usar a galeria como base para um álbum.

## Erro comum: tentar fotografar uma família demasiado grande

Se a vossa família é grande (15+ pessoas: 3 gerações), uma sessão "todos juntos" é difícil de coreografar. **Recomendação**: ou reservar 1h30-2h (em vez de 1h), ou dividir em duas sessões — uma para cada núcleo familiar mais umas quantas fotos com todos.

Tirar 50 pessoas a olharem todas para a câmara ao mesmo tempo é, na prática, impossível. Há sempre alguém a piscar, a olhar para o lado, a perguntar onde está o telemóvel. Profissionais experientes sabem isto e tiram 20-30 disparos do mesmo grupo, sabendo que vai precisar de fundir caras de várias fotos.

## Imprimir, não esquecer

A pior parte de uma sessão profissional é abrir a galeria, dizer "uau, lindas", guardar o link, e nunca mais abrir.

Soluções:
1. **Comprar 1 álbum impresso** logo no momento (40-80 € por um álbum de 20 páginas com qualidade fotográfica).
2. **Imprimir 1-3 fotografias** em formato grande (A3 ou maior) e pendurar em casa.
3. **Encomendar 30 imagens 10×15** para distribuir por avós, padrinhos, tios.

Sem esse passo, a sessão fica num arquivo digital. Com esse passo, fica na vida da família.

## FAQ

### Os meus avós têm 80+ anos. Conseguem fazer uma sessão?
Sim, claro. Avisem o fotógrafo: caminhada curta, banco para descanso, espaço calmo (não Ribeira em Sábado). Quinta do Covelo ou jardins do Palácio de Cristal são óptimos para esta situação.

### Tenho um bebé recém-nascido. Posso?
Sim. Sessões com bebés idealmente em interior controlado (cama, mantas) — chama-se *newborn session* e tem fotógrafos especializados. Mas se quiserem o bebé fora num spot natural, é possível em dias amenos com o bebé bem agasalhado.

### Quanto custa imprimir um álbum bom?
Depende do tamanho e qualidade. Album de fotos de luxo do Porto (acabamento couro, 20-30 páginas, foto de cada lado): 150-300 €. Album simples mas bonito: 60-120 €. Photo Portugal trabalha com gráficas locais que sabem entregar em poucos dias.

### Posso fazer a sessão dentro de minha casa?
Sim, é uma boa opção em dias de chuva — chama-se *lifestyle session at home*. As fotografias têm intimidade que sessões exteriores não têm. Funciona muito bem com bebés e crianças pequenas no seu ambiente.

### Quanto antes devo marcar?
Para Setembro-Outubro (alta procura), **3-4 semanas antes**. Em Junho-Julho **2 semanas antes**. Em Janeiro-Fevereiro pode-se marcar com 1 semana de antecedência se houver disponibilidade.

