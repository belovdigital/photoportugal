- **Do they photograph real people?** Some portfolios are full of model shoots, which look different from real vacation photography. You want to see regular couples, families, and solo travelers looking natural and happy.
- **Do they shoot in locations similar to where you''ll be?** A photographer who knows the local spots will deliver better results than one shooting there for the first time.
- **Does their editing style match your taste?** Some photographers prefer bright, airy edits. Others lean toward moody, dramatic tones. Neither is better — it''s about what you like.

