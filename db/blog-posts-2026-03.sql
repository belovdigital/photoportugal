If you''ve never done a professional photoshoot, here''s exactly what happens so you can relax and enjoy it.

### Before you meet

Your photographer will confirm the meeting point, time, and any final details the day before. Check the weather together and adjust plans if needed.

### The first five minutes

You''ll meet at the agreed spot. Your photographer will chat with you, explain the plan for the route, and maybe adjust your outfit slightly (collar, hair, accessories). This warm-up is important — it helps you relax before the camera comes out.

### The awkward phase (minutes 5-15)

Almost everyone feels stiff at the start. **This is completely normal.** Your photographer knows this and will start with easy actions — walking together, looking at a view, laughing at a joke. By minute 15, most people forget the camera is there.

### The flow (minutes 15 onward)

Once you''re comfortable, the session flows naturally. Your photographer will:

- Guide you to different spots along a planned route
- Suggest poses and movements ("walk toward me," "whisper something funny," "look at each other")
- Capture candid moments between poses — often these are the best shots
- Vary compositions (wide, medium, close-up, detail)
- Watch for the best light and background at each location

### Outfit changes

If you brought a second outfit, your photographer will plan a natural break point, often at a location change. A quick change in a nearby cafe restroom is totally normal.

### Wrapping up

At the end, your photographer will let you know the timeline for delivery and any next steps. Many photographers share a few phone-quality sneak peeks on the spot so you have something to post right away.

