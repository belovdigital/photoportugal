### Languages spoken

If you''re traveling abroad, make sure your photographer speaks your language comfortably. You''ll be working together for an hour or more, and clear communication makes a real difference in the quality of the experience and the photos.

### Packages and pricing transparency

Look for photographers who clearly list what''s included:

- **Session duration** — 30 minutes, 60 minutes, 2 hours?
- **Number of edited photos** — 20, 50, 100?
- **Delivery timeline** — 3 days, 7 days, 14 days?
