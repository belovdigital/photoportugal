export interface PhotoSpot {
  name: string;
  description: string;
  /** Portuguese translations; when absent, EN fallback is used on /pt. */
  namePt?: string;
  descriptionPt?: string;
  /** German translations; when absent, EN fallback is used on /de. */
  nameDe?: string;
  descriptionDe?: string;
  best_time?: string;
  best_timePt?: string;
  best_timeDe?: string;
  tips?: string;
  tipsPt?: string;
  tipsDe?: string;
}

export function spotSlug(name: string): string {
  return name
    .toLowerCase()
    .normalize("NFD")
    .replace(/[\u0300-\u036f]/g, "")
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-+|-+$/g, "");
}

export function getSpot(citySlug: string, slug: string): PhotoSpot | undefined {
  return (photoSpots[citySlug] || []).find((s) => spotSlug(s.name) === slug);
}

/** Returns name/description in the requested locale, falling back to EN. */
export function spotLocalized(spot: PhotoSpot, locale: string) {
  const pt = locale === "pt";
  const de = locale === "de";
  return {
    name: pt && spot.namePt ? spot.namePt : de && spot.nameDe ? spot.nameDe : spot.name,
    description: pt && spot.descriptionPt ? spot.descriptionPt : de && spot.descriptionDe ? spot.descriptionDe : spot.description,
    best_time: pt && spot.best_timePt ? spot.best_timePt : de && spot.best_timeDe ? spot.best_timeDe : spot.best_time,
    tips: pt && spot.tipsPt ? spot.tipsPt : de && spot.tipsDe ? spot.tipsDe : spot.tips,
  };
}

