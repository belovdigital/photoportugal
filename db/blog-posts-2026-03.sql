- Pre-shoot consultation
- The shooting session itself
- Professional editing of all delivered photos
- Online gallery for viewing and downloading
- Personal use license (print, share, post on social media)

### What might cost extra

- Rush delivery (faster than standard turnaround)
- Additional editing requests beyond the standard style
- Commercial use licensing
- Travel to locations outside the photographer''s base city
- Prints or albums

## Tips for Getting the Best Results

You''ve booked your photographer. Here''s how to make sure you get the best possible photos.

### Before the shoot

- **Share inspiration photos.** Send your photographer 5-10 examples of photos you love. This helps them understand your style preferences.
- **Plan your outfits.** Solid colors work better than busy patterns. Coordinate with your travel partner (complementary tones, not matching outfits). Avoid logos and graphics.
- **Get a good night''s sleep.** It sounds obvious, but tired eyes show in photos. Don''t schedule your shoot after an overnight flight.
- **Hydrate and moisturize.** Your skin will thank you, and so will your photos.

### During the shoot

- **Trust your photographer.** They know what looks good on camera. If a pose feels weird, it probably looks great.
- **Move slowly.** Quick movements blur. Walk slowly, turn your head slowly, settle into poses.
- **Interact with each other.** For couples and families — look at each other, not the camera. Whisper, laugh, hug. Genuine interaction always photographs better than posed smiles.
- **Don''t check the camera constantly.** Resist the urge to review every shot. It breaks the flow and eats into your session time. Trust the process.

### After the shoot

- **Be patient with delivery.** Editing takes time. Rushing your photographer leads to lower quality.
- **Share and credit.** When you post on social media, tag your photographer. It''s good etiquette and helps them continue doing what they love.

