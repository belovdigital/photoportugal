Reprogramamos al día siguiente sin coste extra. Las quintas y palacios de Sintra tienen cubiertos espectaculares (galerías, salones interiores) que también funcionan como locación de fotos.

### Hablamos español, no inglés ni portugués. ¿Habrá comunicación?
Portugueses entienden español casi todo. En hoteles 4-5 estrellas, restaurantes Michelin y fotógrafos profesionales: inglés siempre disponible. Para una quinceañera latina, Portugal es probablemente el país europeo donde menos vais a sentir la barrera del idioma.

### Mi familia es vegetariana / kosher. ¿Encontraremos comida?
Sí, sin problema. Lisboa especialmente tiene infinitas opciones vegetarianas, veganas y certificadas kosher. Restaurantes "100% vegan" desde Tartine hasta Princesa do Castelo.

